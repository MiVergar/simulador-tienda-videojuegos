package paquete;

public class Game {
	
	public String title;
	public int price;
	public String genre;
	public String platform;
	public int amount;
	
	public Game(String title, int price, String genre, String platform, int amount ) {
		super();
		this.title = title;
		this.price = price;
		this.genre = genre;
		this.platform = platform;
		this.amount = amount;
	}
}
